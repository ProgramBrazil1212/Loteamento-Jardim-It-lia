function abrirPainelQuadra(quadraId) {
    const lotes = bancoLoteamento[quadraId];
    const corpoTabela = document.getElementById("lista-lotes-corpo");
    
    document.getElementById("modal-titulo-quadra").innerText = "📋 Lotes da " + quadraId.replace("Q", "Quadra ");
    corpoTabela.innerHTML = ""; 

    if (lotes) {
        lotes.forEach(l => {
            let linha = `<tr>
                <td><strong>Lote ${l.lote}</strong></td>
                <td>${l.area} m²</td>
                <td><input type="text" class="input-tel" value="${l.dono}" placeholder="Inserir proprietário..." onchange="atualizarProprietario('${quadraId}', '${l.lote}', this.value)"></td>
                <td><input type="text" class="input-tel" value="${l.tel}" placeholder="Inserir contato..." onchange="atualizarTelefone('${quadraId}', '${l.lote}', this.value)"></td>
            </tr>`;
            corpoTabela.innerHTML += linha;
        });
        document.getElementById("modalLote").style.display = "block";
    }
}

function actualizarProprietario(quadra, lote, novoValor) {
    const lista = bancoLoteamento[quadra];
    const item = lista.find(l => l.lote === lote);
    if(item) {
        item.dono = novoValor;
        console.log(`Proprietário Lote ${lote} [${quadra}] alterado para: ${novoValor}`);
    }
}

function atualizarTelefone(quadra, lote, novoValor) {
    const lista = bancoLoteamento[quadra];
    const item = lista.find(l => l.lote === lote);
    if(item) {
        item.tel = novoValor;
        console.log(`Contato Lote ${lote} [${quadra}] alterado para: ${novoValor}`);
    }
}

function fecharModal() {
    document.getElementById("modalLote").style.display = "none";
}

window.onclick = function(event) {
    const modal = document.getElementById("modalLote");
    if (event.target == modal) { modal.style.display = "none"; }
}
